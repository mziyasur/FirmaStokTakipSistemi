    private void urunkaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_urunkaydetActionPerformed
        
        String sqlurunekle = "insert into urun(kategori_id,urun_id,urunadı,urunadet,alısfiyat,satısfiyat,urunkodu,yuzeycesit,kalınlıkmm)values(?,?,?,?,?,?,?,?,?)";

        try {
            PreparedStatement ps = DBConnection.getConnection().prepareStatement(sqlurunekle);
            ps.setInt(1, Integer.parseInt(kategori_id.getText()));
            ps.setInt(2, Integer.parseInt(urun_id.getText()));
            ps.setString(3, urunadı.getText());
            ps.setInt(4, Integer.parseInt(urunadet.getText()));
            ps.setInt(5, Integer.parseInt(alısfiyat.getText()));
            ps.setInt(6, Integer.parseInt(satısfiyat.getText()));
            ps.setString(7, urunkodu.getText());
            ps.setString(8, yuzeycesit.getText());
            ps.setInt(9, Integer.parseInt(kalınlıkmm.getText()));

            ps.execute();
            JOptionPane.showMessageDialog(null, "Ürün Başarıyla Eklendi.");
            UrunEkleDisplay urunekle = new UrunEkleDisplay();
            urunekle.setVisible(true);
            setVisible(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_urunkaydetActionPerformed

