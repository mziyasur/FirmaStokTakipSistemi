    public ArrayList<Urun> urunList() {
        ArrayList<Urun> urunList = new ArrayList<>();

        PreparedStatement ps;
        ResultSet rs;
        String sqlurun = "select * from urun";

        try {
            ps = DBConnection.getConnection().prepareStatement(sqlurun);
            rs = ps.executeQuery();
            Urun urun;
            while (rs.next()) {
                urun = new Urun(rs.getInt("kategori_id"), rs.getInt("urun_id"), rs.getString("urunadı"), rs.getInt("urunadet"), rs.getInt("alısfiyat"), rs.getInt("satısfiyat"), rs.getString("urunkodu"), rs.getString("yuzeycesit"), rs.getInt("kalınlıkmm"));
                urunList.add(urun);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        return urunList;

    }

    public void show_uruntablo() {
        ArrayList<Urun> list = urunList();
        DefaultTableModel model = (DefaultTableModel) showUrunTable.getModel();
        Object[] row = new Object[9];
        for (int i = 0; i < list.size(); i++) {

            row[0] = list.get(i).getKategori_id();
            row[1] = list.get(i).getUrun_id();
            row[2] = list.get(i).getUrunadı();
            row[3] = list.get(i).getUrunadet();
            row[4] = list.get(i).getAlısfiyat();
            row[5] = list.get(i).getSatisfiyat();
            row[6] = list.get(i).getUrunkodu();
            row[7] = list.get(i).getYuzeycesit();
            row[8] = list.get(i).getKalınlıkmm();
            model.addRow(row);

        }
    }

